package com.example.entity;

public class Textimg {
private String text;
private int img;

public Textimg(String text, int img) {
	super();
	this.text = text;
	this.img = img;
}
public String getText() {
	return text;
}
public void setText(String text) {
	this.text = text;
}
public int getImg() {
	return img;
}
public void setImg(int img) {
	this.img = img;
}

}
